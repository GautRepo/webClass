package com.nt.entity;

public class Student {
	
	private Integer sid;
	private String sname;
	private Float wt;
	
	public Integer getSid() {
		return sid;
	}
	public void setSid(Integer sid) {
		this.sid = sid;
	}
	public String getSname() {
		return sname;
	}
	public void setSname(String sname) {
		this.sname = sname;
	}
	public Float getWt() {
		return wt;
	}
	public void setWt(Float wt) {
		this.wt = wt;
	}
	
	
	

}
